package com.example.rickmorty.model;

public class Location {
    private String name; // Name of the location
    private String url;  // URL of the location

    // Getter for the name of the location
    public String getName() {
        return name;
    }

    // Setter for the name of the location
    public void setName(String name) {
        this.name = name;
    }

    // Getter for the URL of the location
    public String getUrl() {
        return url;
    }

    // Setter for the URL of the location
    public void setUrl(String url) {
        this.url = url;
    }
}
